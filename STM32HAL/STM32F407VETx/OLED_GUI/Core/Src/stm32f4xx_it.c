/* USER CODE BEGIN 1 */
void UART_IDLE_Callback(UART_HandleTypeDef *huart)
{
    //串口1的空闲中断
    if (__HAL_UART_GET_FLAG(huart, UART_FLAG_IDLE) != RESET && huart == (&huart1)) {
        __HAL_UART_CLEAR_IDLEFLAG(huart);//清除中断标志
        HAL_UART_DMAStop(huart);//暂停DMA接收数据
        recv_len = MAX_BUF_LEN - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);//计算当前接收数据长度
        if(recv_len < MAX_BUF_LEN - 1){
            dma_buf[recv_len] = '\0';
        }
        else{
            recv_len = 0;//无效数据
        }
        __HAL_UNLOCK(huart);
        HAL_UART_Receive_DMA(huart,dma_buf,MAX_BUF_LEN);
    }
}
