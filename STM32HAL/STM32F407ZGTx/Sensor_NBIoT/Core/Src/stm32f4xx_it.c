void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim == (&htim2))
    {
        HAL_GPIO_TogglePin(LED0_GPIO_Port,LED0_Pin);
    }
    if (htim == (&htim3))
    {
        USART3_RX_STA |= 1<<15;//标记接收完成
        HAL_TIM_Base_Stop_IT(&htim3);//关闭定时器中断
    }
}

//通过判断接收连续2个字符之间的时间差不大于10ms来决定是不是一次连续的数据.
//如果2个字符接收间隔超过10ms,则认为不是1次连续数据.也就是超过10ms没有接收到
//任何数据,则表示此次接收完毕.zhongduan
//接收到的数据状态
//[15]:0,没有接收到数据;1,接收到了一批数据.
//[14:0]:接收到的数据长度
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if(huart == (&huart3))
    {
        if((USART3_RX_STA&(1<<15)) == 0)//接收完的一批数据,还没有被处理,则不再接收其他数据
        {
            if(USART3_RX_STA<USART3_MAX_RECV_LEN)	//还可以接收数据
            {
                __HAL_TIM_SET_COUNTER(&htim3,0);	//计数器清空
                if(USART3_RX_STA == 0)
                {
                    HAL_TIM_Base_Start_IT(&htim3);//使能定时器3中断
                }
                USART3_RX_BUF[USART3_RX_STA++] = rx3_data;	//记录接收到的值
            }else
            {
                USART3_RX_STA |= 1<<15;				//强制标记接收完成
            }
        }
        HAL_UART_Receive_IT(huart, &rx3_data, 1); // 重新开启接收中断
    }
}
